function calcEffectiveness(type1, type2, return4Levels = false) {
    if (type2 == undefined || type2 == null) {
        type2 = "None"
    }
    var t1Index = pokemonTypes.indexOf(type1);
    var t2Index = pokemonTypes.indexOf(type2);
    if (t1Index < 0) {
        console.log("failed t1Index " + t1Index);
        return {};
    } if (t2Index < 0) {
        console.log("failed t2Index " + t2Index);
        return {};
    }
    var result = {
        "0": [],
        "0.25": [],
        "0.5": [],
        "1": [],
        "2": [],
        "4": [],
    };
    if (return4Levels) {
        result = {
            "0": [],
            "0.5": [],
            "1": [],
            "2": [],
        };
    }
    for (var i = 0; i < pokemonTypes.length - 1; i++) {
        var t1v2 = typeEffectivenessTable[i][t1Index];
        var t2v2 = typeEffectivenessTable[i][t2Index];
        if (t2v2 == undefined) {
            t2v2 = 1;
        }
        var v = Math.round(t1v2 * t2v2 * 100) / 100;

        if (return4Levels) {
            if (v == 0.25) {
                v = 0.5;
            }
            else if (v == 4) {
                v = 2
            }
        }
        result[v.toString()].push(pokemonTypes[i]);
    }
    return result;
}